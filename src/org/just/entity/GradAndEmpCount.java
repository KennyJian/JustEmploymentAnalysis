package org.just.entity;

public class GradAndEmpCount {

	private String college;
	private String major;
	private int graduate_count;
	private int employment_count;
	private double scale;
	public String getCollege() {
		return college;
	}
	public void setCollege(String college) {
		this.college = college;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public int getGraduate_count() {
		return graduate_count;
	}
	public void setGraduate_count(int graduate_count) {
		this.graduate_count = graduate_count;
	}
	public int getEmployment_count() {
		return employment_count;
	}
	public void setEmployment_count(int employment_count) {
		this.employment_count = employment_count;
	}
	public double getScale() {
		return scale;
	}
	public void setScale(double scale) {
		this.scale = scale;
	}

}
